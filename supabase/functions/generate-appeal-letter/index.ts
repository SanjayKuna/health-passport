import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { patientName, diagnosis, medicines, denialReason, claimDate } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY not configured");

    const medicineList = (medicines || [])
      .map((m: { name: string; dosage: string }) => `- ${m.name} (${m.dosage})`)
      .join("\n");

    const prompt = `You are a medical insurance appeals specialist. Generate a professional appeal letter for a denied insurance claim.

Patient: ${patientName}
Diagnosis: ${diagnosis}
Claim Date: ${claimDate}
Denial Reason: ${denialReason || "Insufficient clinical evidence"}
Prescribed Medications:
${medicineList}

Write a formal, professional appeal letter addressed to the Insurance Medical Director. The letter should:
1. Reference the specific claim and denial reason
2. Provide clinical justification for the prescribed treatments
3. Cite that the medications are standard-of-care for the diagnosis
4. Request reconsideration with supporting clinical rationale
5. Be respectful but firm in tone

Format the letter properly with date, addressee, subject line, body paragraphs, and closing.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: "You are a medical insurance appeals expert. Write clear, professional appeal letters." },
          { role: "user", content: prompt },
        ],
      }),
    });

    if (!response.ok) {
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limited, try again shortly." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      throw new Error("AI gateway error");
    }

    const result = await response.json();
    const letter = result.choices?.[0]?.message?.content || "Unable to generate letter.";

    return new Response(JSON.stringify({ letter }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  } catch (e) {
    console.error("Error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
